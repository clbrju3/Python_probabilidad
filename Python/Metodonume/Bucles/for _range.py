#Para no tener que crear una coleccion se usa el objeto range para que itere en nuestro bucle
for i in range(10):
    print(i)
for i in range(3,8):#Primer espacio valor de inicio, segundo valor final que no se toma y tecero diferencia entre elementos 
    print(i)
for i in range(3,8,2):
    print(i)
